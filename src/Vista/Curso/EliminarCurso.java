    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        CursoVo curso = new CursoVo();
        try {
            
        
        curso = coordinador.buscarCurso(Integer.parseInt(txtCodigo.getText()));
        if (curso == null) {
            return;
        } else {
            cargaTxt(curso);
            onCampos();
        }
        } catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null,"Por favor ingrese un código de curso");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

