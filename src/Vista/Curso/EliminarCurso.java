    public int asignaCurso() {
        Random rnd = new Random();
        int codigo = (int) (rnd.nextDouble() * 1000 + 1);
        while (coordinador.existeCurso(codigo)) {
            codigo = (int) (rnd.nextDouble() * 1000 + 1);
            System.out.println(codigo);
        }
        return codigo;
    }

