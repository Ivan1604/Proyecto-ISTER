    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        int resultado;
        VentanaFoto foto = new VentanaFoto();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("JPG Y PNG", "jpg", "png");
        foto.jFileChooser1.setFileFilter(filtro);
        resultado = foto.jFileChooser1.showOpenDialog(null);
        if (JFileChooser.APPROVE_OPTION == resultado) {
            fichero = foto.jFileChooser1.getSelectedFile();
            try {
                ImageIcon icon = new ImageIcon(fichero.toString());
                Icon icono = new ImageIcon(icon.getImage().getScaledInstance(labelFoto.getWidth(), labelFoto.getHeight(), Image.SCALE_DEFAULT));
                labelFoto.setText("");
                labelFoto.setIcon(icono);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Error abriendo la imagen " + ex);

            }

        }
    }//GEN-LAST:event_btnCargarActionPerformed


