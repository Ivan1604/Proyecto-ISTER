    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (validaCampos()) {
            String cedula = txtCedula.getText();
            String[] nombres = txtNombres.getText().split(" ");
            if (nombres.length != 2) {
                JOptionPane.showMessageDialog(null, "Ingrese los dos nombres e intente de nuevo.", "Error de Registro", JOptionPane.WARNING_MESSAGE);
                return;
            }
            String[] apellidos = txtApellidos.getText().split(" ");
            if (apellidos.length != 2) {
                JOptionPane.showMessageDialog(null, "Ingrese los dos apellidos e intente de nuevo.", "Error de Registro", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (!txtPass1.getText().equals(txtPass.getText())) {
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden. Intente de nuevo", "Error de Registro", JOptionPane.WARNING_MESSAGE);
                return;
            }
            int resp = JOptionPane.showConfirmDialog(null, "¿Está seguro?");
            if (resp == 0) {
                Date date = fecha.getDate();
                String telefono = txtTelefono.getText();
                String mail = txtEmail.getText();
                int creditos = Integer.parseInt(txtCreditos.getText());
                int creditos1 = Integer.parseInt(txtCreditos.getText());
                String pass = txtPass.getText();
                DocenteVo docente = new DocenteVo(cedula, nombres[0].toUpperCase(), nombres[1].toUpperCase(), apellidos[0].toUpperCase(), apellidos[1].toUpperCase(), telefono, mail, pass, creditos, creditos1, date, fichero);
                coordinador.editarDocente(docente);
                coordinador.setDocenteVo(docente);
            }
            return;
        } else {
            JOptionPane.showMessageDialog(null, "LLene todos los campos e inténte de nuevo", "Campos sin llenar", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    public void cargaTxt() {
        DocenteVo docente = new DocenteVo();
        docente = coordinador.getDocenteVo();
        txtCedula.setText(docente.getCedula());
        txtNombres.setText(docente.getNombre1() + " " + docente.getNombre2());
        txtApellidos.setText(docente.getApellido1() + " " + docente.getApellido2());
        fecha.setDate(docente.getFecha());
        txtEmail.setText(docente.getMail());
        txtTelefono.setText(docente.getTelefono());
        txtCreditos.setText(String.valueOf(docente.getCreditos()));
        txtPass.setText(docente.getPass());
        fichero = docente.getFoto();
        txtPass1.setText(docente.getPass());
        ImageIcon icon = new ImageIcon(fichero.toString());
        Icon icono = new ImageIcon(icon.getImage().getScaledInstance(170, 140, Image.SCALE_DEFAULT));
        labelFoto.setText("");
        labelFoto.setIcon(icono);
    }

