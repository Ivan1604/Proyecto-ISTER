    public void actualizarTablaCoincidenciasDocentes(ArrayList<DocenteVo> docentes1) {
        md1 = new DefaultTableModel(data1, cabeza1) {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        this.tabla.setModel(md1);
        for (int i = 0; i < docentes1.size(); i++) {
            String arr[] = {docentes1.get(i).getCedula(), docentes1.get(i).getNombre1(), docentes1.get(i).getApellido1(), docentes1.get(i).getMateria(), String.valueOf(docentes1.get(i).getCreditos())};
            md1.addRow(arr);
        }
    }


